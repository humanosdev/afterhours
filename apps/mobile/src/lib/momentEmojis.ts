function toItems(emojis: string[], prefix: string): MomentEmojiItem[] {
  return emojis.map((emoji, index) => ({
    id: `emoji:${prefix}:${index}:${emoji}`,
    emoji,
  }));
}

export const MOMENT_EMOJI_RAIL: MomentEmojiItem[] = [
  ...toItems(SMILEYS, "smileys"),
  ...toItems(PEOPLE, "people"),
  ...toItems(ANIMALS, "animals"),
  ...toItems(FOOD, "food"),
  ...toItems(ACTIVITIES, "activities"),
  ...toItems(TRAVEL, "travel"),
  ...toItems(OBJECTS, "objects"),
  ...toItems(SYMBOLS, "symbols"),
];

export function filterMomentEmojis(query: string): MomentEmojiItem[] {
  const q = query.trim().toLowerCase();
  if (!q) return MOMENT_EMOJI_RAIL;
  return MOMENT_EMOJI_RAIL.filter((item) => item.emoji.includes(q) || item.id.includes(q));
}
